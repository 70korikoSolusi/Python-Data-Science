# tipe data number 
panjang = 5
lebar = 10.5
luas = panjang * lebar
print(panjang, '*', lebar, '=', luas)
print("Tipe dari variabel panjang:", type(panjang))
print("Tipe dari variabel lebar:", type(lebar))
print("Tipe dari variabel luas:", type(luas))
